package W3Resource.basic_part1;

public class CompareTwoNumbers {
    public static void main(String[] args) {
        int n1 =25;
        int n2 = 39;
        if(n1 != n2 && n1<n2 && n1<=n2){
            System.out.println(n1+" != "+n2);
            System.out.println(n1+" < "+n2);
            System.out.println(n1+" <= "+n2);
        }
    }
}
