package com.JTraining;

public class RelationalOperators {
    public static void main(String[] args) {
        int x = 5;
        int y = 9;
        //is equal to
        System.out.println(x == y); // false (5 == 9)
        //not equal to
        System.out.println(x != y); // true ( 5 != 9)
        //greater than
        System.out.println(x > y); //false(5 > 9)
        //lesser than
        System.out.println(x < y); //true(5 < 9)
        //greater than or equal to
        System.out.println(x >= y); //false(5 >= 9)
        //lesser than or equal to
        System.out.println(x <= y); //true(5 <= 9)
    }
}
